void error_init()
{
	memset(&errors,0,sizeof(errors));
}
//-----------------------------------------------------------------------------------
void error_insert(u16 code)
{
	u8 i;
	for(i=MAX_ERRORS_NUM-1;i!=0;i--)	
	{
		errors.item[i]=errors.item[i-1];
	}
	errors.item[0].code=code;
	errors.item[0].time=MenuRegs.Time;	
	errors.new_error=1;
//	Time_To_Store=3;	
}
